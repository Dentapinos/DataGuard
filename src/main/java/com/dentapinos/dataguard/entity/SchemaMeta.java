package com.dentapinos.dataguard.entity;

import java.util.List;

/**
 * Метаданные схемы базы данных.
 * <p>
 * Описывает структуру всей базы данных на момент создания бэкапа:
 * имя базы и список таблиц с их метаданными.
 * <p>
 * Используется при:
 * <ul>
 *   <li>экспорте — для сохранения структуры БД в бэкап;</li>
 *   <li>импорте/восстановлении — для воссоздания таблиц, ключей и индексов.</li>
 * </ul>
 *
 * @param database имя базы данных (схемы) в СУБД, например {@code "center_beer"}.
 * @param tables   список описаний таблиц, входящих в данную базу (каждая таблица
 *                 представлена объектом {@link TableMeta}).
 */
public record SchemaMeta(
        String database,
        List<TableMeta> tables
) {}
