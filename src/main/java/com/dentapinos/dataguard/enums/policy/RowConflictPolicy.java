package com.dentapinos.dataguard.enums.policy;

/**
 * Политика обработки конфликтов строк при восстановлении (например, дублирование PK/UNIQUE).
 * Определяет действие при обнаружении конфликта уникальных ключей.
 */
public enum RowConflictPolicy {
    /**
     * Прерывание восстановления: любая ошибка конфликта приводит к прерыванию процесса.
     * Гарантирует целостность данных, но может остановить восстановление на середине.
     */
    FAIL_ON_CONFLICT,

    /**
     * Пропуск конфликтующих строк: строки с конфликтами игнорируются, остальные восстанавливаются.
     * Подходит для сценариев, где допустимо частичное восстановление.
     */
    SKIP_ON_CONFLICT,

    /**
     * Перезапись существующих строк: данные из бэкапа перезаписывают существующие записи в БД.
     * Используется для полного обновления данных в целевой таблице.
     */
    OVERWRITE_ON_CONFLICT
}
