package com.dentapinos.dataguard.enums.policy;

/**
 * Политика проверки схемы базы данных при восстановлении.
 * Определяет, насколько строго должна совпадать схема целевой БД со схемой из бэкапа.
 */
public enum SchemaPolicy {
    /**
     * Строгая проверка схемы: схема должна полностью совпадать.
     * Любые расхождения (лишние/отсутствующие колонки, таблицы) вызывают ошибку.
     */
    STRICT_SCHEMA,

    /**
     * Упрощённая проверка: допустимы расхождения в схеме.
     * Лишние или отсутствующие колонки игнорируются, восстановление продолжается.
     */
    RELAXED_SCHEMA,

    /**
     * Автоматическое создание: недостающие таблицы создаются в целевой БД.
     * Позволяет восстанавливать данные в частично или полностью пустую БД.
     */
    AUTO_CREATE_TABLES
}
