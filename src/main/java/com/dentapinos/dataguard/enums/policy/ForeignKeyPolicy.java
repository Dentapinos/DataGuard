package com.dentapinos.dataguard.enums.policy;


/**
 * Политика работы с внешними ключами (FK) при восстановлении.
 * Управляет проверкой ограничений целостности связей между таблицами.
 */
public enum ForeignKeyPolicy {
    /**
     * Полная проверка: все внешние ключи активны, нарушения вызывают ошибку.
     * Обеспечивает максимальную целостность данных, но требует корректной последовательности восстановления.
     */
    ENFORCE_ALL,

    /**
     * Временное отключение: FK отключаются на время восстановления (например, FOREIGN_KEY_CHECKS=0 в MySQL).
     * Ускоряет восстановление, но временно нарушает целостность связей.
     */
    TEMP_DISABLE,

    /**
     * Пропуск нарушений: попытки вставки продолжаются, нарушения логируются и пропускаются.
     * Балансирует скорость и целостность: часть данных восстанавливается, ошибки фиксируются для последующего анализа.
     */
    SKIP_VIOLATIONS
}
