package com.dentapinos.dataguard.enums.policy;

/**
 * Политика реакции на ошибки во время восстановления.
 * Определяет общую стратегию обработки ошибок.
 */
public enum ErrorPolicy {
    /**
     * Немедленное прерывание: первая же ошибка останавливает процесс восстановления.
     * Подходит для критических сценариев, где любая ошибка недопустима.
     */
    FAIL_FAST,

    /**
     * Продолжение с логированием: ошибки записываются в лог, восстановление продолжается, где возможно.
     * Максимизирует объём восстановленных данных, позволяет анализировать проблемы постфактум.
     */
    LOG_AND_CONTINUE
}
