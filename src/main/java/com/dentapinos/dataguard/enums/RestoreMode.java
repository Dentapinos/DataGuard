package com.dentapinos.dataguard.enums;


/**
 * Перечисление, определяющее режим восстановления данных из резервной копии.
 * Каждый режим определяет стратегию обработки конфликтов и изменений при восстановлении схемы и данных.
 */
public enum RestoreMode {
    /**
     * Строгое восстановление: требуется полное соответствие схемы и данных из бэкапа.
     * В случае конфликта (например, существующие данные отличаются от бэкапа) — операция завершается с ошибкой.
     */
    STRICT,

    /**
     * Мягкое слияние: пропускает конфликтующие строки без ошибки, допускает небольшие расхождения в схеме (например, новые колонки).
     * Подходит для постепенного обновления и тестируемых операций.
     */
    SAFE_MERGE,

    /**
     * Агрессивная замена данных из бэкапа: данные бэкапа считаются «истиной» и полностью заменяют текущие данные.
     * Существующие строки перезаписываются, новые добавляются. Не затрагивает строки, отсутствующие в бэкапе.
     */
    FORCE_REPLACE,

    /**
     * Режим «только добавление»: новые строки добавляются, существующие (по первичному ключу) — не меняются и не удаляются.
     * Гарантирует сохранение всех текущих данных, добавляет только то, чего не было.
     */
    APPEND_ONLY,

    /**
     * Режим «сухого запуска»: симуляция восстановления без реального изменения данных.
     * Используется для проверки совместимости бэкапа с текущей БД и предсказания поведения.
     */
    DRY_RUN,

    /**
     * Только проверка совместимости схемы без восстановления данных.
     * Проверяет, можно ли применить бэкап без ошибок на текущей схеме (например, совпадают ли типы колонок, есть ли обязательные поля).
     */
    SAFE_SCHEMA_CHECK,

    /**
     * Полная операция UPSERT: каждая строка из бэкапа либо обновляет существующую запись (по первичному ключу), либо добавляется как новая.
     * Полностью заменяет данные в пределах уникального идентификатора.
     */
    UPSERT_ALL
}