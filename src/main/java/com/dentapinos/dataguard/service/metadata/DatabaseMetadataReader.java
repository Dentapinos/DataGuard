package com.dentapinos.dataguard.service.metadata;

import com.dentapinos.dataguard.dto.DbCredentials;
import com.dentapinos.dataguard.entity.SchemaMeta;

import java.util.List;

/**
 * Интерфейс для чтения метаданных из базы данных.
 * <p>
 * Предоставляет методы для получения схемы базы данных (структура таблиц, отношений и т.д.)
 * и списка всех доступных таблиц в указанной базе данных.
 * </p>
 *
 * @see DbCredentials Данные для подключения к базе данных
 * @see SchemaMeta Метаданные схемы базы данных
 */
public interface DatabaseMetadataReader {

    /**
     * Считывает полную метаданную схемы указанной базы данных.
     * <p>
     * Метод выполняет подключение к базе данных и извлекает информацию о её структуре,
     * включая список таблиц, их поля, типы данных и ограничения (constraints).
     * </p>
     *
     * @param credentials        Учётные данные для подключения к базе данных
     * @param database           Имя базы данных, метаданные которой необходимо получить
     * @param includedTables     Список таблиц, которые необходимо включить в метаданные.
     *                           Если {@code null} или пустой список — включаются все таблицы.
     * @return {@link SchemaMeta} объект, содержащий полную метаданную схемы базы данных.
     * @throws IllegalStateException если произошла ошибка при чтении метаданных.
     *                               Подробная информация об ошибке может быть предоставлена
     *                               в текстовом сообщении исключения.
     */
    SchemaMeta readSchema(DbCredentials credentials,
                          String database,
                          List<String> includedTables);

    /**
     * Получает список всех таблиц в указанной базе данных.
     * <p>
     * Метод выполняет подключение к базе данных и извлекает имена всех таблиц,
     * доступных в указанной базе данных.
     * </p>
     *
     * @param credentials Учётные данные для подключения к базе данных
     * @param database    Имя базы данных, из которой необходимо получить список таблиц
     * @return Список имен таблиц (строки) в указанной базе данных.
     *         Может быть пустым, если база данных не содержит таблиц.
     * @throws IllegalStateException если произошла ошибка при получении списка таблиц.
     *                               Подробная информация об ошибке может быть предоставлена
     *                               в текстовом сообщении исключения.
     */
    List<String> listTables(DbCredentials credentials, String database);
}
